def calculate_prism(h, x1, y1, x2, y2, x3, y3):
    """Высота, площадь основания треугольника и объём треугольной призмы."""  # описание возвращаемых значений
    # Считаем стороны основания по координатам трёх точек
    a = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5  # длина стороны между точками 1 и 2
    b = ((x3 - x2) ** 2 + (y3 - y2) ** 2) ** 0.5  # длина стороны между точками 2 и 3
    c = ((x1 - x3) ** 2 + (y1 - y3) ** 2) ** 0.5  # длина стороны между точками 1 и 3

    p = (a + b + c) / 2  # полупериметр треугольника основания
    S_osn = (p * (p - a) * (p - b) * (p - c)) ** 0.5  # площадь основания по формуле Герона

    V = S_osn * h  # объём призмы: V = S_осн * h
    return h, S_osn, V  # возвращаем кортеж: высота, площадь основания, объём
