import math  # импортируем math для корня и других функций


def heron_area(a, b, c):
    """Площадь треугольника по формуле Герона."""  # краткое описание функции
    if a <= 0 or b <= 0 or c <= 0:  # стороны должны быть больше нуля
        raise ValueError("Стороны должны быть положительными")  # сообщение об ошибке

    # проверяем, что треугольник существует по неравенству треугольника
    if a + b <= c or a + c <= b or b + c <= a:
        raise ValueError("Треугольника с такими сторонами не существует")  # если не выполняется — ошибка

    p = (a + b + c) / 2  # полупериметр треугольника
    return math.sqrt(p * (p - a) * (p - b) * (p - c))  # формула Герона для площади
