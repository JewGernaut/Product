from cylinder_volume import cylinder_area  # функция для площади цилиндра
from triangle_area_heron import heron_area  # функция площади треугольника (Герон)
from rectangle_area import rectangle_area  # функция площади прямоугольника
from circke_area import circke_area  # функция площади круга
from prism_volume import calculate_prism  # функция высоты, площади основания и объёма призмы


def main():
    """Главная функция с текстовым меню."""  # короткое описание функции
    while True:  # бесконечный цикл, пока пользователь не выберет 0
        s = input('''
Для выбора пункта в меню выберите цифру
1) Рассчитать площадь прямоугольника
2) Вычисляет площадь круга
3) Высчитывает объем цилиндра
4) Формула Герона для площади треугольника
5) ДОП ЗАДАНИЕ!!
0) Выход
Ввод: ''')  # выводим меню и читаем выбор
        menu = int(s)  # превращаем введённую строку в число

        if menu == 1:  # пункт 1 — прямоугольник
            print("Вы выбрали расчитать площадь прямоугольника")  # пояснение выбора
            l = float(input("Введите длину прямоугольника: "))  # ввод длины
            w = float(input("Введите ширину прямоугольника: "))  # ввод ширины
            try:
                area = rectangle_area(l, w)  # вызываем функцию площади прямоугольника
                print("Площадь прямоугольника:", area)  # вывод результата
            except ValueError as e:  # обработка ошибки (например, отрицательные числа)
                print(e)

        elif menu == 2:  # пункт 2 — круг
            print("Вычислить площадь круга")  # пояснение выбора
            r = float(input("Введите радиус круга: "))  # ввод радиуса
            try:
                area = circke_area(r)  # вызываем функцию площади круга
                print("Площадь круга", area)  # вывод результата
            except ValueError as e:
                print(e)

        elif menu == 3:  # пункт 3 — цилиндр
            print("Вычислить объем цилиндра")  # пояснение выбора
            r = float(input("Введите радиус цилиндра: "))  # ввод радиуса цилиндра
            h = float(input("Введите высоту цилиндра: "))  # ввод высоты цилиндра
            try:
                area = cylinder_area(r, h)  # считаем площадь поверхности цилиндра
                print("Площадь цилиндра:", area)  # вывод результата
            except ValueError as e:
                print(e)

        elif menu == 4:  # пункт 4 — формула Герона
            print("Вычислить площадь треугольника (формула Герона)")  # пояснение выбора
            a = float(input("Введите сторону a: "))  # ввод стороны a
            b = float(input("Введите сторону b: "))  # ввод стороны b
            c = float(input("Введите сторону c: "))  # ввод стороны c
            try:
                area = heron_area(a, b, c)  # считаем площадь по Герону
                print("Площадь треугольника:", area)  # вывод результата
            except ValueError as e:
                print(e)

        elif menu == 5:  # пункт 5 — доп. задание с призмой
            print("ДОП ЗАДАНИЕ!")  # заголовок блока
            h = float(input("Введите высоту призмы: "))  # ввод высоты призмы
            x1 = float(input("Введите x1: "))  # координата x первой точки
            y1 = float(input("Введите y1: "))  # координата y первой точки
            x2 = float(input("Введите x2: "))  # координата x второй точки
            y2 = float(input("Введите y2: "))  # координата y второй точки
            x3 = float(input("Введите x3: "))  # координата x третьей точки
            y3 = float(input("Введите y3: "))  # координата y третьей точки

            try:
                area = calculate_prism(h, x1, y1, x2, y2, x3, y3)  # считаем высоту, площадь основания и объём призмы
                print("Вывод:\n 1)Высота 2)Площадь основания 3)Объем \n", area)  # выводим кортеж с результатами
            except ValueError as e:
                print(e)

        elif menu == 0:  # пункт 0 — выход из программы
            print("Пока!")  # прощальное сообщение
            break  # выходим из цикла, программа заканчивается

        else:  # если номер пункта не существует
            print("Такого выбора нет")  # сообщение об ошибочном выборе


main()  # запуск главной функции, чтобы программа работала при запуске файла
