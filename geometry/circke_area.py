import math  # подключаем модуль math для числа π


def circke_area(radius):
    """Площадь круга по радиусу."""  # объяснение назначения функции
    if radius <= 0:  # радиус должен быть больше нуля
        raise ValueError("Радиус не может быть меньше либо равен 0")  # сообщение об ошибке
    return math.pi * radius ** 2  # формула площади круга S = π * r^2
