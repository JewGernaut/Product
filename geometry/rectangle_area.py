def rectangle_area(length, width):
    """Возвращает площадь прямоугольника."""  # короткое описание функции
    if length < 0 or width < 0:  # проверяем, что стороны не отрицательные
        raise ValueError("Длина и ширина не могут быть отрицательными")  # ошибка, если меньше нуля
    elif length == 0 or width == 0:  # отдельная проверка на нулевую сторону
        raise ValueError("Такого прямоугольника нет")  # сообщение, что прямоугольник не существует
    return length * width  # считаем площадь S = a * b и возвращаем
